    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        try {
            // Declara las variables para preparar y ejecutar la consulta SQL
            PreparedStatement ps = null;
            ResultSet rs = null;

            // conexi√≥n a la base de datos
            ConeccionDB conn = new ConeccionDB();
            Connection con = conn.getConexion();

            //preparar la consulta sql y guardar el nuevo dato a la base de datos
            ps = con.prepareStatement("INSERT INTO empleado (nombre, cedula, edad, telefono, correo, cargo, direccionVivienda) VALUES (?,?,?,?,?,?,?)");
            ps.setString(1, txtNombre.getText());
            ps.setString(2, txtCedulaBuscar.getText());
            ps.setString(3, txtEdad.getText());
            ps.setString(4, txtTelefono.getText());
            ps.setString(5, txtCorreo.getText());
            ps.setString(6, txtCargo.getText());
            ps.setString(7, txtDireccion.getText());

            ps.execute();

            JOptionPane.showMessageDialog(null, "empleado guardado");
            Object[] fila = new Object[7];
            fila[0] = txtNombre.getText();
            fila[1] = txtCedulaBuscar.getText();
            fila[2] = txtEdad.getText();
            fila[3] = txtTelefono.getText();
            fila[4] = txtCorreo.getText();
            fila[5] = txtCargo.getText();
            fila[6] = txtDireccion.getText();
            modelo.addRow(fila);
            limpiarCampos();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al guardar al empleado");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

